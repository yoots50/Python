import turtle

turtle.title("오각형 그리기")
turtle.shape("turtle")

angle = 72
dist = 100

turtle.forward(dist)
turtle.right(angle)
turtle.forward(dist)
turtle.right(angle)
turtle.forward(dist)
turtle.right(angle)
turtle.forward(dist)
turtle.right(angle)
turtle.forward(dist)
turtle.right(angle)

turtle.done()
